import java.util.*;
class CodeChef {
    public static void main(String[] args){
        Scanner s=new Scanner(System.in);
             char c=s.next().charAt(0);
            if(c=='A'||c=='E'||c=='I'||c=='O'||c=='U'){
                System.out.println("Vowel");
            }else{
                System.out.println("Consonant");
            }
    
}
}
